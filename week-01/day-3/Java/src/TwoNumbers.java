public class TwoNumbers {

    public static void main(String[] args) {

        int a = 13;
        int b = 22;
        double c = 13.0;
        double d = 22.0;

        System.out.println(b + a);
        System.out.println(a - b);
        System.out.println(b * a);
        System.out.println(d / c);
        System.out.println(b / a);
        System.out.println(b % a);

    }
}
